fn align_up(v: u32, align: u32) -> u32 { (v + align - 1) & !(align - 1) }

fn patch_u32(buf: &mut [u8], offset: usize, value: u32) {
    buf[offset..offset + 4].copy_from_slice(&value.to_le_bytes());
}

fn build_pe(shellcode: &[u8], bits: u32) -> Vec<u8> {
    let (header, p_code, p_image, p_virt, p_raw) = match bits {
        32 => (PE32_HEADER.as_ref(), P32_SIZE_OF_CODE, P32_SIZE_OF_IMAGE, P32_VIRT_SIZE, P32_RAW_SIZE),
        _  => (PE64_HEADER.as_ref(), P64_SIZE_OF_CODE, P64_SIZE_OF_IMAGE, P64_VIRT_SIZE, P64_RAW_SIZE),
    };

    let raw_size = align_up(shellcode.len() as u32, FILE_ALIGNMENT);
    let img_size = align_up(RVA_TEXT + raw_size, SECTION_ALIGNMENT);
    let padding  = FILE_ALIGNMENT as usize - header.len();

    let mut buf = Vec::with_capacity(FILE_ALIGNMENT as usize + raw_size as usize);
    buf.extend_from_slice(header);
    buf.extend(std::iter::repeat(0u8).take(padding));
    buf.extend_from_slice(shellcode);
    buf.extend(std::iter::repeat(0u8).take(raw_size as usize - shellcode.len()));

    patch_u32(&mut buf, p_code,  raw_size);
    patch_u32(&mut buf, p_image, img_size);
    patch_u32(&mut buf, p_virt,  shellcode.len() as u32);
    patch_u32(&mut buf, p_raw,   raw_size);

    buf
}

fn decode_hex(s: &str) -> Option<Vec<u8>> {
    let s = s.trim().trim_start_matches("0x");
    if s.is_empty() || s.len() % 2 != 0 { return None; }
    s.chars().all(|c| c.is_ascii_hexdigit()).then(|| {
        (0..s.len())
            .step_by(2)
            .map(|i| u8::from_str_radix(&s[i..i + 2], 16).unwrap())
            .collect()
    })
}

fn resolve_shellcode(arg: &str) -> io::Result<Vec<u8>> {
    let path = Path::new(arg);
    if path.exists() {
        return fs::read(path);
    }
    decode_hex(arg).ok_or_else(|| {
        io::Error::new(io::ErrorKind::InvalidInput,
            "argument is neither an existing file nor a valid hex string")
    })
}

fn main() -> io::Result<()> {
    let args: Vec<String> = env::args().collect();
    if args.len() < 2 || args.len() > 3 {
        eprintln!("Usage: {} <shellcode.bin | hex_string> [32|64]", args[0]);
        std::process::exit(1);
    }

    let bits: u32 = match args.get(2).map(String::as_str) {
        Some("32") => 32,
        Some("64") | None => 64,
        Some(v) => {
            eprintln!("[!] invalid mode '{}': expected 32 or 64", v);
            std::process::exit(1);
        }
    };

    let input_arg = &args[1];
    let input_path = Path::new(input_arg);

    let shellcode = resolve_shellcode(input_arg)?;

    let stem = if input_path.exists() {
        input_path.file_stem().unwrap_or(input_path.as_os_str()).to_string_lossy().into_owned()
    } else {
        "shellcode".to_string()
    };
    let output_path = input_path.with_file_name(format!("{}_sh.exe", stem));

    let input_display = if input_path.exists() {
        fs::canonicalize(input_path)?.display().to_string()
    } else {
        "<hex string>".to_string()
    };

    eprintln!("[*] input:    {}", input_display);
    eprintln!("[*] mode:     PE{} ({}-bit)", bits, bits);
    eprintln!("[*] payload:  {} bytes", shellcode.len());

    let pe = build_pe(&shellcode, bits);

    fs::write(&output_path, &pe)?;
    let output_display = fs::canonicalize(&output_path)?.display().to_string();
    eprintln!("[*] output:   {}", output_display);
    eprintln!("[+] done — entry point: 0x00401000");

    Ok(())
}