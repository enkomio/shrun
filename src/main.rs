// ── helpers ───────────────────────────────────────────────────────────────────
fn align_up(v: u32, align: u32) -> u32 {
    (v + align - 1) & !(align - 1)
}

fn patch_u32(buf: &mut [u8], off: usize, val: u32) {
    buf[off..off + 4].copy_from_slice(&val.to_le_bytes());
}

// ── build_pe ──────────────────────────────────────────────────────────────────
// File layout:
//   0x000          PE headers (padded to FILE_ALIGNMENT)
//   0x200          .text      shellcode, padded to FILE_ALIGNMENT   RVA 0x1000
//   0x200+text_raw .rdata     import table, 0x200 bytes fixed       RVA varies
//
// Patched fields:
//   SizeOfCode, SizeOfImage, .text VirtualSize, .text SizeOfRawData,
//   .rdata PointerToRawData
fn build_pe(shellcode: &[u8], bits: u32) -> Vec<u8> {
    let (header, rdata, p_code, p_image, p_tvirt, p_traw, p_rptr) = if bits == 32 {
        (
            &PE32_HEADER[..], &RDATA32[..],
            P32_CODE, P32_IMAGE, P32_TVIRT, P32_TRAW, P32_RPTR,
        )
    } else {
        (
            &PE64_HEADER[..], &RDATA64[..],
            P64_CODE, P64_IMAGE, P64_TVIRT, P64_TRAW, P64_RPTR,
        )
    };

    let text_raw  = align_up(shellcode.len() as u32, FILE_ALIGNMENT);
    let rdata_rva = align_up(RVA_TEXT + text_raw, SECTION_ALIGNMENT);
    let rdata_ptr = FILE_ALIGNMENT + text_raw;        // file offset of .rdata
    let img_size  = align_up(rdata_rva + SECTION_ALIGNMENT, SECTION_ALIGNMENT);
    let hdr_pad   = FILE_ALIGNMENT as usize - header.len();

    let total = FILE_ALIGNMENT as usize + text_raw as usize + rdata.len();
    let mut buf = Vec::with_capacity(total);

    // headers
    buf.extend_from_slice(header);
    buf.extend(std::iter::repeat(0u8).take(hdr_pad));

    // .text
    buf.extend_from_slice(shellcode);
    buf.extend(std::iter::repeat(0u8).take(text_raw as usize - shellcode.len()));

    // .rdata (fixed 0x200 bytes)
    buf.extend_from_slice(rdata);

    // patch header fields
    patch_u32(&mut buf, p_code,  text_raw);
    patch_u32(&mut buf, p_image, img_size);
    patch_u32(&mut buf, p_tvirt, shellcode.len() as u32);
    patch_u32(&mut buf, p_traw,  text_raw);
    patch_u32(&mut buf, p_rptr,  rdata_ptr);

    // If shellcode > one SA page, .rdata RVA shifts.
    // Re-patch section VA and the two data-directory RVAs in the header.
    if rdata_rva != RVA_RDATA_DEFAULT {
        let delta = rdata_rva - RVA_RDATA_DEFAULT;

        // .rdata VirtualAddress sits at section_start + 12
        let rdata_va_off = if bits == 32 { 0x160 + 12 } else { 0x170 + 12 };
        patch_u32(&mut buf, rdata_va_off, rdata_rva);

        // Dir[1] Import RVA at known positions in header
        let dir1_off  = if bits == 32 { 0x0b8 + 1*8  } else { 0x0c8 + 1*8  };
        let dir12_off = if bits == 32 { 0x0b8 + 12*8 } else { 0x0c8 + 12*8 };
        let old_dir1  = u32::from_le_bytes(buf[dir1_off..dir1_off+4].try_into().unwrap());
        let old_dir12 = u32::from_le_bytes(buf[dir12_off..dir12_off+4].try_into().unwrap());
        patch_u32(&mut buf, dir1_off,  old_dir1  + delta);
        patch_u32(&mut buf, dir12_off, old_dir12 + delta);

        // Fix every absolute RVA embedded inside the .rdata section itself.
        // These are all u32 values in the first 0x70 bytes of .rdata.
        let rdata_start = FILE_ALIGNMENT as usize + text_raw as usize;
        let rva_slots: &[usize] = if bits == 64 {
            // DESC.ILT, DESC.Name, DESC.IAT, ILT[0] lo-u32, IAT[0] lo-u32
            &[0x00, 0x0c, 0x10, 0x28, 0x38]
        } else {
            // DESC.ILT, DESC.Name, DESC.IAT, ILT[0], IAT[0]
            &[0x00, 0x0c, 0x10, 0x18, 0x20]
        };
        for &s in rva_slots {
            let abs = rdata_start + s;
            let old = u32::from_le_bytes(buf[abs..abs+4].try_into().unwrap());
            if old != 0 {
                patch_u32(&mut buf, abs, old + delta);
            }
        }
    }

    buf
}

// ── hex decode ────────────────────────────────────────────────────────────────
fn decode_hex(s: &str) -> Option<Vec<u8>> {
    let s = s.trim().trim_start_matches("0x");
    if s.is_empty() || s.len() % 2 != 0 { return None; }
    if !s.chars().all(|c| c.is_ascii_hexdigit()) { return None; }
    Some((0..s.len())
        .step_by(2)
        .map(|i| u8::from_str_radix(&s[i..i+2], 16).unwrap())
        .collect())
}

fn resolve_shellcode(arg: &str) -> io::Result<Vec<u8>> {
    let p = Path::new(arg);
    if p.exists() { return fs::read(p); }
    decode_hex(arg).ok_or_else(|| io::Error::new(
        io::ErrorKind::InvalidInput,
        "argument is neither an existing file nor a valid hex string",
    ))
}

// ── main ──────────────────────────────────────────────────────────────────────
fn main() -> io::Result<()> {
    let args: Vec<String> = env::args().collect();
    if args.len() < 2 || args.len() > 3 {
        eprintln!("Usage: {} <shellcode.bin | hex_string> [32|64]", args[0]);
        std::process::exit(1);
    }

    let bits: u32 = match args.get(2).map(String::as_str) {
        Some("32")        => 32,
        Some("64") | None => 64,
        Some(v) => {
            eprintln!("[!] invalid mode '{}': expected 32 or 64", v);
            std::process::exit(1);
        }
    };

    let input_arg  = &args[1];
    let input_path = Path::new(input_arg);
    let shellcode  = resolve_shellcode(input_arg)?;

    let stem = if input_path.exists() {
        input_path.file_stem()
            .unwrap_or(input_path.as_os_str())
            .to_string_lossy()
            .into_owned()
    } else {
        "shellcode".to_string()
    };
    let output_path = input_path.with_file_name(format!("{}_sh.exe", stem));

    let input_display = if input_path.exists() {
        fs::canonicalize(input_path)?.display().to_string()
    } else {
        "<hex string>".to_string()
    };

    eprintln!("[*] input:   {}", input_display);
    eprintln!("[*] mode:    PE{} ({}-bit)", bits, bits);
    eprintln!("[*] payload: {} bytes", shellcode.len());

    let pe = build_pe(&shellcode, bits);
    fs::write(&output_path, &pe)?;

    let out_display  = fs::canonicalize(&output_path)?.display().to_string();
    let image_base: u64 = if bits == 32 { 0x0040_0000 } else { 0x0000_0001_8000_0000 };
    let entry_va = image_base + RVA_TEXT as u64;

    eprintln!("[*] output:  {}", out_display);
    eprintln!("[+] done  —  entry point: {:#018x}", entry_va);

    Ok(())
}